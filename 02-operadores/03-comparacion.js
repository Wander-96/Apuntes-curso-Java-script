// Los operadores de comparacion se utilizan para poder comparar distintas variables entre si o con otra...

//Op. Relacionales
let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Op. de Igualdad."==" pregunta si los valores son iguales, no si los tipos son iguales. Por eso number10 y string 10 son true.
//"===" estricta igualdad si difiere tipos

console.log(a ==10); // es mejor no usarlos para evitar erroes
console.log(a !=10); // es mejor no usarlos para evitar erroes
console.log(a === '10'); // string
console.log(a !== '10'); //son distintos tipos de dato? SI
